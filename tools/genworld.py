# -*- coding: utf-8 -*-
# ワールドマップ60x46を領域定義から生成して JS リテラルを出力する
W, H = 60, 46
g = [['~'] * W for _ in range(H)]

def fill(x1, y1, x2, y2, ch):
    for y in range(y1, y2 + 1):
        for x in range(x1, x2 + 1):
            g[y][x] = ch

def put(x, y, ch):
    g[y][x] = ch

# ---- やみの島（オペラハウス） x8-18, y3-11 ----
fill(8, 3, 18, 11, 'd')
for x in range(8, 19):
    put(x, 3, 'm'); put(x, 11, 'm')
for y in range(3, 12):
    put(8, y, 'm'); put(18, y, 'm')
put(13, 11, 'g')          # ゲート（けっかい）
put(13, 6, 'X')           # オペラハウス

# ---- ゆきの大陸 x24-52, y3-12 ----
fill(24, 3, 52, 12, 'S')
fill(36, 5, 38, 5, 'm')
put(44, 6, 'm'); put(46, 6, 'm')
put(45, 6, 'i')           # こおりの大聖堂
put(28, 9, '5')           # ユキネ

# ---- ノワールの小島 x54-57, y7-8 ----
fill(54, 7, 57, 8, 'S')

# ---- ザルバの小島 x20-23, y13-14 ----
fill(20, 13, 23, 14, 'd')

# ---- 火山島 x8-18, y15-23 ----
fill(8, 15, 18, 23, 'd')
for (x, y) in [(9,16),(10,16),(15,16),(16,16),(9,19),(17,19),(9,20),(10,20),(15,20),(16,20)]:
    put(x, y, 'm')
put(10, 17, 'L'); put(16, 17, 'L')
put(13, 18, 'v')          # 火山

# ---- はじまりのほこらの島 x3-6, y15-18 ----
fill(3, 15, 6, 18, '.')
put(4, 16, 'e')

# ---- しらべの塔の島 x25-31, y17-22 ----
fill(25, 17, 31, 22, '.')
fill(27, 17, 29, 17, 'F')
fill(26, 22, 27, 22, 'F')
fill(29, 22, 30, 22, 'F')
put(28, 19, 't')

# ---- かくれざとの島 x49-57, y14-20 ----
fill(49, 14, 57, 20, 'F')
put(53, 17, '6')

# ---- はじまりの大陸 x29-58, y24-43 ----
fill(29, 24, 58, 43, '.')
# 北の森（旧聖堂）
fill(45, 24, 56, 27, 'F')
put(52, 25, 'f')
# 王都・港
put(41, 28, '2')
put(29, 29, '3')
# 山脈（とうげ x44 と かいがん x58 が通り道）
fill(31, 32, 43, 32, 'm')
fill(45, 32, 57, 32, 'm')
# 南部のしげみ
fill(33, 34, 37, 34, ',')
fill(35, 38, 39, 38, ',')
fill(41, 40, 47, 40, ',')
fill(53, 36, 55, 36, ',')
put(48, 38, '1')          # ミルテ村
put(56, 36, 'c')          # こだまの洞窟
fill(45, 37, 48, 37, 'm')
# ---- クラーケンの小島 x24-26, y34-36 ----
fill(24, 34, 26, 36, '.')

# ---- さばくの大陸 x3-21, y28-43 ----
fill(3, 28, 21, 43, 's')
fill(8, 31, 10, 31, 'm')
fill(14, 36, 16, 36, 'm')
fill(12, 42, 15, 42, 'm')
put(12, 33, '4')          # オアシア
put(7, 39, 'p')           # ピラミッド

# ---- 出力 ----
rows = [''.join(r) for r in g]
assert all(len(r) == W for r in rows)
for ch in '123456cfpvitXeg':
    n = sum(r.count(ch) for r in rows)
    assert n == 1, f"{ch}: {n}"
for i, r in enumerate(rows):
    print(f'"{r}", // {i}')
